<script setup>
    const props = defineProps(['tarefas'])
</script>
<template>
        <ul class="list list-group mt-4" v-if="props.tarefas.length > 0">
            <li class="list-group-item" v-for="tarefa in props.tarefas">
            <input @change="evento => tarefa.finalizada = evento.target.checked" :checked="tarefa.finalizada" type="checkbox" :id="tarefa.titulo">
            <label :class="{ done: tarefa.finalizada === true }" class="ms-3" :for="tarefa.titulo">{{ tarefa.titulo }}</label>
            </li>
        </ul>
        <p class="text-muted" v-else>Não há tarefas pendentes!</p>
</template>

<style scoped>
    .done {
        text-decoration: line-through;
    }
</style>
